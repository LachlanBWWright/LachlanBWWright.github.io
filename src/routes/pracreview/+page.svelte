      <p class="font-bold">
        Principles of sustainability to create viable systems
      </p>

      <p>
        My internship primarily revolved around the development of a website
        that effectively served as a structured whiteboard, that could be used
        collaboratively amongst different users in a manner similar to a product
        like Google Docs. While initial development, which continued from a
        foundation made by a previous group of interns, occurred at a high pace,
        the complexity of the website caused development to slow down over time.
        This slowed development was largely the consequence of the
        software&rsquo;s structure being a poor fit for the complex nature of
        the website, causing issues and bugs that forced the team to spend time
        addressing, as well as making significant changes to the
        website&rsquo;&rsquo;s structure. This eventually culminated in a
        product demonstration to the client where the website, which is in a
        state that had not seen significant developments since it showcased a
        week prior, displayed a number of errors and issues.
      </p>

      <p>
        Naturally, this experience caused a large deal of frustration, in
        contrast to the more pleasant experience at the commencement of the
        internship. Additionally, while the fast pace of early development
        helped me feel confident in my capabilities, feeling the rate of
        development slow or even appear to regress at some points hurt my
        confidence, and caused me to feel anxious about whether the goals of the
        project could be completed within the timeframe of the internship, as
        well as how my capability was being perceived by the client.
      </p>

      <p>
        I personally felt that the best course of action for the project would
        be to recognize the importance of planning, while also avoiding wasting
        significant amounts of time on it, and also believing that it would be
        best to make some changes to the previous group&rsquo;s foundations
        instead of immediately starting to build upon it, while not throwing it
        out entirely, even if it was imperfect.
      </p>

      <p>
        Finding the right balance between allocating your time making
        foundational improvements, planning for future developments, and
        actually spending time implementing these developments is a difficult
        task, primarily relying on making an accurate prediction on the extent
        to which planning and making foundational improvements will save time in
        the future. As a result, the optimal course of action is not always
        evident, and is highly dependent on the context. During the internship,
        the decision to focus primarily on development seemed to be correct
        initially, but was proven incorrect over time.
      </p>

      <p>
        In software development, the consequence for focusing on developing
        quickly at the expense of slower development in future is characterised
        as &lsquo;technical debt&rsquo;. As is the case with monetary debt,
        there are often considered to be good and bad types of technical debt,
        depending on whether the benefits of incurring the debt is worth the
        consequence (Asana, 2024). Likewise, the decision to redo existing,
        imperfect elements of the software can often prove worthwhile, but now
        always (Jarosz, n.d.).
      </p>

      <p>
        The experience changed my beliefs regarding the balance between making
        immediate developments or planning and ensuring a solid foundation in
        favour of the latter, while still recognising that it is fundamentally a
        balancing act, and that it is possible to go too far in the opposite
        direction. If I had my time again, I would have spent substantially more
        time making these improvements at the beginning, which I believe would
        have assisted with the outcomes of the project substantially.
      </p>

      <br />
      <p class="font-bold">
        Professional Practice within intercultural and global contexts.
      </p>

      <p>
        Towards the later stages of the internship, and particularly during the
        final week, the process of handing over from the team of interns to a
        consultancy firm, which primarily revolved around providing the
        firm&rsquo;s members an understanding of the technical foundations of
        the website as to facilitate further development. This firm had a number
        of members based in Australia, as well as a number of members in
        European countries. As a result, the team of interns had primarily
        direct, face-to-face communication with the Australian members, while
        also communicating with the European members over online video calls
        mired with technical difficulties.
      </p>

      <p>
        Consequently, communication with the European members was significantly
        more strained, despite all of them being fluent English speakers, with
        my general impression being that the European consultants would
        effectively have to gain most of their technical understanding from the
        Australian consultants, who had been communicated with in a much more
        effective manner.
      </p>

      <p>
        I was mildly surprised by the structure of the consultancy firm, with my
        general expectation being that such a firm would either be entirely
        located within Australia, or almost entirely located internationally,
        instead of the &ldquo;fifty-fifty&rdquo; split it exhibited. Otherwise,
        I naturally felt somewhat frustrated by the technical issues that
        occurred during the online video conferences, particularly given that it
        was one of the limited changes that we had with the European
        consultants. This sense of frustration and disappointment ultimately
        extended to my impression at the end of the internship, believing that
        the hindered communication would have a detrimental, albeit not
        catastrophic, impact on future development.
      </p>

      <p>
        I personally feel that international communication in the context of an
        engineering project poses significant communication challenges,
        primarily due to cultural and linguistic barriers, the difficulty of
        managing different time zones, as well as a belief that conference
        calling will always be an inferior alternative to meeting in-person.
      </p>

      <p>
        As has been explored, the nature of international communication creates
        some significant difficulties. These difficulties were exacerbated by
        technical difficulties, primarily related to audio, during conference
        calls that largely occurred as a result of the Australian
        consultant&rsquo;s technical unfamiliarity with the client&rsquo;s
        location.
      </p>

      <p>
        Often, it is recommended that written documentation and tools such as
        Trello or Slack are used as part of the process of handing over from one
        team to another (UtilitiesOne, 2023). While these tools were likely to
        be used between the European and Australian consulting teams, they were
        not used between the Intern team and the European consultants. In a
        similar vein, it is also commonly recommended to have a formal, written
        agenda before engaging in an international meeting (Fernandez et al.,
        2023).
      </p>

      <p>
        The nature of the consulting firm showed that participating in
        professional international communication is likely to be even more
        prevalent than I was predisposed to believe. Additionally, the
        experience showcased the importance of written communication and
        preparation for communication over long distances, and particularly in
        an international context.
      </p>

